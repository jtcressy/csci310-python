class AsciiArt:
    def __init__(self, input):
        self.input = str(input).upper()
        self.output = str()
        self.lines = list()

    def __len__(self):
        return len(self.input)

    def __str__(self):
        lines = list()
        for idx in range(0, len(self.ascii_art["A"])):
            line = str()
            for letter in self.input:
                line += self.ascii_art[letter][idx]
            lines.append(line + "\n")
        for line in lines:
            self.output += line
        return self.output

